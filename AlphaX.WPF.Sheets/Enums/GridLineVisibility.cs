namespace AlphaX.WPF.Sheets
{
    public enum GridLineVisibility
    {
        /// <summary>
        /// Grid lines will not be rendered.
        /// </summary>
        None,
        /// <summary>
        /// Both horizontal/vertical grid lines will be rendered.
        /// </summary>
        Both,
        /// <summary>
        /// Only horizontal grid lines will be rendered.
        /// </summary>
        Horizontal,
        /// <summary>
        /// Only vertical grid lines will be rendered.
        /// </summary>
        Vertical
    }
}
